// Global Function for getting table data


function todolist(value) {
    document.getElementById('tbody').innerHTML = "";
    for (i = 0; i < value.length; i++) {
        let tbody = document.getElementById('tbody');
        let trow = document.createElement('tr');
        let tdata1 = document.createElement('td');
        let tdata2 = document.createElement('td');
        let tdata3 = document.createElement('td');


        tbody.appendChild(trow);
        trow.appendChild(tdata1);
        trow.appendChild(tdata2);
        trow.appendChild(tdata3);
        tdata1.innerHTML = value[i].id;
        tdata1.style.fontWeight = "bold";
        tdata2.innerHTML = value[i].title;

        // condition on completed section

        if (value[i].completed == true) {
            tdata3.innerHTML = "Completed";
            tdata3.style.color = "green";
        }
        else {
            tdata3.innerHTML = "Pending";
            tdata3.style.color = "red";
        }
    }
    console.log(value)
}


/* function on input search. When user put any value related title.
 Table will show searched title row */

function filter() {

    let newarrFilter = myarr.filter(function (y) {

        let search = document.getElementById('search').value;
        let getarr = (y.title).includes(search);

        if (getarr == true) {
            return true;
        } else {
            return false;
        };
    })

    todolist(newarrFilter)
}


// Function on dropdown to get "Completed" , "Uncompleted" & "All" separately

function dropdown(event) {

    // getting array of completed elements

    let newarr = myarr.filter(function (n) {

        let getarr = (n.completed == true);

        if (getarr == true) {
            return true;
        } else {
            return false;
        };
    })

    // calling function when user click on completed at dropdown

    if (event.target.value == "Completed") {
        todolist(newarr);
    }


    // getting filter array of uncompleted elements

    let newarr1 = myarr.filter(function (m) {

        let getarr = (m.completed == false);

        if (getarr == true) {
            return true;
        } else {
            return false;
        };
    })

    // calling function when user click on Uncompleted at dropdown

    if (event.target.value == "Uncompleted") {
        todolist(newarr1);
    }


    // getting all elements of arrays

    if (event.target.value == "All") {
        todolist(myarr)
    }

}

